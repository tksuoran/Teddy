/*!  \page page_getting Getting Teddy to your machine

<em>Latest sources for Teddy are currently not available
through CVS.</em>

To get started, you need the sources. You have two options
here: source file archive and CVS. However, using the CVS
requires that you first have the latest source file archive,
so you must always start by downloading the latest source file
archive.

\section section_archive_files Getting Teddy by using source archive file

%Teddy source file archives can be downloaded from these locations:

<ul>
<li><a href="https://sourceforge.net/project/showfiles.php?group_id=40550">
%Teddy download page at Sourceforge</a> Latest official releases
<li><a href="http://www.cs.helsinki.fi/u/tksuoran/TeddyBeta.html">
Teddy Beta pages</a> Early beta releases
</ul>

Following archives are currently or soon available:

<ul>
<li><em>TeddySrc-x.xx.tgz</em> is the source arhive. You
need this archive to compile %Teddy. This is a complete
archive containing source and all data files required to
build %Teddy on any system. This archive also includes
precompiled libraries and headers for Windows Visual C++,
but not for linux.

<li><em>TeddyWin32Demo-x.xx.zip</em> is the Windows demo
archive. This archive contains demo executable and required
runtime libraries and data files for Windows. Sources are not
included in this archive.
</ul>

\section section_cvs Getting Teddy by using CVS

If you plan to make contributions, you should use the concurrent
version control system, CVS.

To use CVS, you will need:

<ul>
<li>Sourceforge account and membership of %Teddy project.
This enables developer access to %Teddy CVS.
<li>Source file archive, unpacked. This contains the required
runtime data files and build target directory structure.
</ul>

Oops, the documentation ends here currently. Please mail me if you
have problems.

See also \ref page_cvs_support
 *********************************************************************************/



